					¿CUÁL ES MEJOR CÁMARA RÉFLEX: CANON O NIKON?
					Tanto Canon como Nikon son grandes marcas de prestigioso renombre, marcas que demostraron a lo largo de muchos años su valía y su preocupación por ofrecerle al usuario un producto bueno, de altas prestaciones, y que sea la mejor combinación posible de prestaciones, precio y calidad.

					Te digo una cosa: si te encuentras con alguien que te recomiende efusivamente una de las dos marcas pero criticando la otra marca, no te fíes, esa persona no sabe lo que dice. En serio. En este campo de la cámaras réflex existen muchos fanatismos, personas que por haber tenido y tratado toda la vida con una marca X creen que ésa es la mejor marca del mundo, y así se lo recomiendan a sus amigos y familiares.

					Puedo entender que porque hayan tenido experiencias positivas con una marca en particular la quieran promover y recomendar, hasta ahí todo bien, lo que no me parece adecuado es que se esmeren en desacreditar a la otra marca, sólo por fanatismo, muchas veces con mucho desconocimiento.

					La mayoría de las personas nos inclinamos hacia una marca u otra, Canon o Nikon (u otras marcas) casi por casualidad: o porque algún amigo haya tenido esa marca, o porque su precio nos haya parecido más asequible, etc. Pero a día de hoy, no existe ningún consenso entre los expertos de que una marca sea CLARAMENTE mejor que la otra. Conozco a gente felizmente enamorada de las Nikon (como es el caso de un servidor), o sea, nikonistas 😉 , así como otros para los que la fotografía es sinónimo de la palabra «Canon» y que se sienten muy canonistas.

					Si buscas en la mayoría de los blogs, foros y páginas webs análisis y críticas de Canon vs. Nikon siempre encontrarás experiencias buenas y malas. Para mí son siempre las experiencias buenas las que cuentan, ya que las malas la mayoría de las veces provienen del hecho de que la gente se compra cámaras sin saber primero qué es lo que buscan, pues hay una cámara para cada uno de nosotros y hay que saber dar con ella 😉

					Dicho esto, pasemos a los hechos y datos concretos.

								AYUDA
					La mayoría de las réflex digitales de Nikon (creo que todas) llevan un pequeño botón con un punto de interrogación, para ayuda rápida. En cuanto lo tocas te explican la función en la que estás en ese momento. Canon no tiene ese botoncito, tendrás que ir siempre al librito ;-P, aunque ahora en algunas cámaras ha incluido una guía de ayuda para quienes se inician y cuentan con una aplicación con tutoriales, consejos y demás ayuda.

					Como ves, cada una de las dos grandes marcas tiene sus ventajas y sus inconvenientes, así que no te comas la cabeza, por favor no te vuelvas loco, sé que una compra de este calibre es una decisión que hay que tomar muy detenidamente, pero muchas veces podemos caer en el error de escuchar una opinión, luego otra, luego otra, y otra y otra, y justo cuando estamos a punto de realizar la compra, volvemos a leer otra opinión, y nos volvemos locos.

					Espero que ahora lo tengas un poco más claro. Ni Canon es mejor que Nikon ni al revés. Dilo por ahí please, se agradecen recomendaciones y votos 😉

